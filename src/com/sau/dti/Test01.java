//Encapsulation
package com.sau.dti;

public class Test01 {
    //Data member
    private int dataA = 10;
    private double dataB = 22.222;
    private String dataC = "Hello";

    //Method member
    public int getDataA() {
        return dataA;
    }

    public void setDataA(int dataA) {
        this.dataA = dataA;
    }

    public double getDataB() {
        return dataB;
    }

    public void setDataB(double dataB) {
        this.dataB = dataB;
    }

    public String getDataC() {
        return dataC;
    }

    public void setDataC(String dataC) {
        this.dataC = dataC;
    }
}
